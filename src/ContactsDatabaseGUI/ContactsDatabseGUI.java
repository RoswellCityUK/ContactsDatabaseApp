    public final List<Contacts> getContacts() {
        Query query = eman.createNamedQuery("Contacts.findAll");
        List<Contacts> contacts = query.getResultList();
       
        return contacts;
    }
    
    public void populateComboBox(JComboBox <Contacts> comboContacts) {
        
        for (Contacts contact : contacts) {
            comboContacts.addItem(new Contacts(contact.getId(), contact.getFirstName(), contact.getLastName(), contact.getPhoneNumber()));
        }
       
    }
    
    public void addContact(Contacts contact){
        eman.getTransaction().begin();
        eman.persist(contact);
        eman.getTransaction().commit();
    }
    
    public void updateContact(Contacts contact){
        eman.getTransaction().begin();
        eman.merge(contact);
        eman.getTransaction().commit();
    }
    
    public void deleteContact(int ID){
        Contacts contact = eman.find(Contacts.class, ID);
        eman.getTransaction().begin();
        eman.remove(contact);
        eman.getTransaction().commit();
    }
    
    private void populateFields(Contacts contact){
        txtID.setText(contact.getId().toString());
        
        txtName.setText(contact.getFirstName());
        
        txtSurname.setText(contact.getLastName());
        
        txtPhoneNumber.setText(contact.getPhoneNumber());
        
        SimpleDateFormat dt1 = new SimpleDateFormat("dd/MM/yyyy");
        txtDOB.setText(dt1.format(contact.getBirthday()));
        
        rbtnIsFriendYes.setSelected(contact.getFriend());
        rbtnIsFriendNo.setSelected(!contact.getFriend());
        
        rbtnIsEnemyYes.setSelected(contact.getEnemy());
        rbtnIsEnemyNo.setSelected(!contact.getEnemy());
    }
    
    private void clearAllFields(){
        txtID.setText("");
        
        txtName.setText("");
        
        txtSurname.setText("");
        
        txtPhoneNumber.setText("");
        
        txtDOB.setText("");
        
        rbtnIsFriendYes.setSelected(true);
        rbtnIsFriendNo.setSelected(false);
        
        rbtnIsEnemyYes.setSelected(true);
        rbtnIsEnemyNo.setSelected(false);
    }
    
