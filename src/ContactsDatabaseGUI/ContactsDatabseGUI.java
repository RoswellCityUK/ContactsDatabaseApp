    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try{
            SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy", Locale.ENGLISH);

            Date date = formatter.parse(txtDOB.getText());

            Contacts contact = new Contacts();
            if(!isNewRecord)
                contact.setId(Integer.valueOf(txtID.getText()));
            
            contact.setFirstName(txtName.getText());
            contact.setLastName(txtSurname.getText());
            contact.setPhoneNumber(txtPhoneNumber.getText());
            contact.setBirthday(date);
            
            if(rbtnIsFriendYes.isSelected() && !rbtnIsFriendNo.isSelected()){
                contact.setFriend(true);
            }else{
                contact.setFriend(false);
            }
            if(rbtnIsEnemyYes.isSelected() && !rbtnIsEnemyNo.isSelected()){
                contact.setEnemy(true);
            }else{
                contact.setEnemy(false);
            }
            if(isNewRecord){
                addContact(contact);
                isNewRecord = false;
            }else{
                updateContact(contact);
            }
            
        }
        catch(ParseException e)
        {
            e.printStackTrace();
        }
        int temp = cboxSelectedItem.getSelectedIndex();
        contacts = getContacts();
        cboxSelectedItem.setModel(new DefaultComboBoxModel(contacts.toArray()));
        if(isNewRecord)
            temp = cboxSelectedItem.getItemCount() - 1;
        cboxSelectedItem.setSelectedIndex(temp);
    }//GEN-LAST:event_btnSubmitActionPerformed

